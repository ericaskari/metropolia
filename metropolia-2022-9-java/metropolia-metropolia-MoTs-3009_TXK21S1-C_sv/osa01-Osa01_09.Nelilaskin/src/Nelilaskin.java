
public class Nelilaskin {

    public static void main(String[] args) {
        int eka = 5;
        int toka = 3;

        System.out.printf("%d + %d = %d\n", eka, toka, eka + toka);
        System.out.printf("%d - %d = %d\n", eka, toka, eka - toka);
        System.out.printf("%d * %d = %d\n", eka, toka, eka * toka);
        System.out.printf("%d / %d = %f\n", eka, toka, 1.0 * eka / toka);
    }
}
